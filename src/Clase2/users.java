    private void lblImagenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImagenMouseClicked
        JFileChooser chooser=new JFileChooser();
        chooser.setDialogTitle("Eliga una imagen");
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int option=chooser.showOpenDialog(this);
        if(option==JFileChooser.APPROVE_OPTION){
            this.colocarImagen(chooser.getSelectedFile().getAbsolutePath());
        }else{
            JOptionPane.showMessageDialog(this, "No has seleccionado una imagen", "Imagen no seleccionada", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_lblImagenMouseClicked

