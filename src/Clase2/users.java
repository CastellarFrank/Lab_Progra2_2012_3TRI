    private void colocarImagen(String path){
        ImageIcon imagen=new ImageIcon(path);
        ImageIcon foto=new ImageIcon(imagen.getImage().getScaledInstance(this.lblImagen.getWidth(),this.lblImagen.getHeight(),Image.SCALE_SMOOTH));
        this.lblImagen.setIcon(foto);
    }
